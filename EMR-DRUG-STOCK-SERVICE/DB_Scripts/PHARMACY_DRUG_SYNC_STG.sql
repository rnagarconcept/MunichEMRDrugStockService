(
  ID                   NUMBER NOT NULL, 
  DRUG_CODE		       NVARCHAR2(255), 
  DRUG_QTY             NUMBER(10,2),
  CREATED_ON           DATE DEFAULT SYSDATE, 
  UPDATED_ON           DATE 
  )
TABLESPACE USERS
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
