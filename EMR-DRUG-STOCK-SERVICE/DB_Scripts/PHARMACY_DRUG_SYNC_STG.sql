CREATE UNIQUE INDEX PHARMACY_DRUG_SYNC_STG_PK ON PHARMACY_DRUG_SYNC_STG
(ID)
LOGGING
TABLESPACE USERS
PCTFREE    10
INITRANS   2
MAXTRANS   255
STORAGE    (
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
